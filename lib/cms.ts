export const getContent = cache(async (): Promise<CMS> => {
  try {
    const rows = await db.content.findMany()
    const obj: CMS = { ...DEFAULTS }
    for (const r of rows) obj[r.key] = r.value
    return obj
  } catch {
    return { ...DEFAULTS }
  }
})

export function cms(content: CMS, key: string, fallback?: string): string {
  return content[key] ?? fallback ?? DEFAULTS[key] ?? ''
}
